package org.task;

public class Switch {
	public static void main(String[] args) {
		int sum=144;
		switch (sum) {
		case 1:
			System.out.println("sum is 1");
			break;
		
		case 10:
			System.out.println("sum is 10");
			break;
		default:
			System.out.println("default value is 0");
			break;
		}
	}
}
	
			
	